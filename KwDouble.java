public class KwDouble
{
    public static void main(String[] args)
    {
        double num1 = 5.75;
        double num2 = 2.5;

        double sum = num1+num2;
        System.out.println("Number 1: " +num1);
        System.out.println("Number 2: " +num2);
        System.out.println("Sum: "+sum);
    }
}