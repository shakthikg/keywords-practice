public class KwFloat {
    public static void main(String[] args) {
        float number1 = 3.14f;
        float number2 = 5.67f;
    
        float sum = number1 + number2;
            
        System.out.println("Number 1: " + number1);
        System.out.println("Number 2: " + number2);
        System.out.println("Sum: " + sum);
        
    }
}
