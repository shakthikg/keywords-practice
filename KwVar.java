public class KwVar {
    public static void main(String[] args) {
        var message = "Hello, World!"; 
        var number = 42;               
        var decimal = 3.14;            
        System.out.println("Message: " + message);
        System.out.println("Number: " + number);
        System.out.println("Decimal: " + decimal);
    }
}
